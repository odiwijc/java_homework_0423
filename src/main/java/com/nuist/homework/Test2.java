package com.nuist.homework;

public class Test2 {
    public static void main(String[] args) {
        // 2、演示基本数据类型转换(强制转换、隐式转换)
        // 强制转换
        byte i = (byte)128;
        System.out.println(i);
        // 隐式转换
        int a = 111;
        long j = a;
        System.out.println(j);
    }
}
